          <h2>L'homme derrière le clown</h2>
          <p>
            <strong>Pascal BOFFY alias BOBOF,</strong> est un gymnaste de haut
            niveau devenu acrobate se révélant être un clown de cirque comme il
            en existe trop peu aujourd’hui, ainsi qu'un artiste complet qui a su
            préserver l’âme, la sensibilité et la force de l’Auguste, en
            pointant son nez rouge dans un univers décalé de l’espace temps.
            <em>Bobof</em> apporte donc sa part à l’éternité des clowns. Fidèle
            des Maîtres du genre, <strong>Grock</strong>,
            <strong>le trio Fratellini</strong>, <strong>les Barrios</strong>, <strong>Charlie Chaplin</strong> et <strong>Buster Keaton</strong>
            il proclame ainsi bien haut que les clowns sont des artistes à part
            entière et que la pureté de leur art doit permettre d’atteindre les
            petits comme les grands. Il fait partie de ceux qui ont contribué à
            la naissance du cirque contemporain. <strong>En 1981,</strong> il
            fut le co-fondateur de <em>la Compagnie de la Bulle,</em> une bande
            de fous musico théâtreux circassiens
            <em
              >(le Ministère de la Culture reconnaît leur talent et leur confie
              donc des spectacles),</em
            >
            puis le duo Gus et Balthazar qui tourne dans la France entière. Le
            café-théâtre aussi, l’encadrement de stages , les spectacles de rue… </br>
          </p>
           </div>
          </div>
        </div>
      

        <p></br>
          Il suit plusieurs stages á l’école
          <strong>Annie Fratellini</strong> et s’astreint également à des
          entraînements quotidiens de jonglerie et d’équilibre. Bobof est appelé
          par <strong>l’UNESCO</strong> pour la création d’un spectacle sur
          <em>les Droits de l’Enfant.</em> Il travaille dans différents cirques,
          côtoie <strong>Roger Lanzac</strong> et <strong>Bouglione.</strong>
          <strong>En 1991,</strong> il accomplit enfin son rêve : l’ère du
          cirque Bobof débute lorsqu’il fait l’acquisition de son propre
          chapiteau. <strong>En 2001</strong> Bobof est récompensé pour son
          talent en décrochant le <strong>Nez d’Or</strong> au
          <em>Festival International du Clown de Monté-Carlo.</em> Depuis, il
          poursuit sa destinée en présentant des créations toujours originales,
          en organisant des stages de cirque dans toute la France et prenant
          part à l’action de la
          <em>Fédération Française des Ecoles de Cirque</em>. Parallèlement, il
          met à disposition son talent de clown à des actions humanitaires à
          travers la planète, notamment dans les pays de l’Est et dans les camps
          de réfugiés tibétains en l’Himalaya.
        </p>

