              <h1>Qui sommes-nous ?</h1>
              <p>
                <strong>Le Cirque Bobof :</strong> Une Compagnie de cirque
                itinérante basée dans l’Est de la France
                <strong>(Haute-Saône)</strong>. Nous dispensons des
                spectacles de cirque contemporains pour enfants et adultes
                , ainsi que des stages de cirque pour débutants,
                amateurs et professionnels, et nous pratiquons aussi des
                locations de chapiteaux pour divers évènements. Fort d’une longue
                expérience dans le monde du cirque,
                <strong>Pascal Boffy</strong> met ses talents d’artiste et de
                pédagogue au service de projets culturels d’envergure en France
                ainsi qu'à l’étranger. De ville en ville, le Cirque se monte et
                se démonte, tel un écosystème éphémère, en semant sur son
                passage de belles rencontres, d’ inoubliables souvenirs et des
                étoiles plein les yeux.
              </p>
            </div>

